

public class User {
    private int id;
    private String name;

    // 构造函数
    // 无参
    public User() {
    }
    // 有参
    public User(int id, String name) {
        this.id = id;
        this.name = name;
    }


    // Setter、Getter

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
